            _dropDownBorder = new Pen(KnownColors.DropDownBorder);
            _fontBlack = new Pen(KnownColors.FontBlack);
            _fontGray = new Pen(KnownColors.FontGray);
            _highlightYellow = new Pen(KnownColors.HighlightYellow);
            _linkBlue = new Pen(KnownColors.LinkBlue);
            _markupDuplicates = new Pen(KnownColors.MarkupDuplicates);
            _markupStatusBarWarning = new Pen(KnownColors.MarkupStatusBarWarning);
            _menuIconBackground = new Pen(KnownColors.MenuIconBackground);
            _menuIconPressed = new Pen(KnownColors.MenuIconPressed);
            _titleBlue = new Pen(KnownColors.TitleBlue);
            _titleRed = new Pen(KnownColors.TitleRed);
            _white = new Pen(KnownColors.White);
        }

